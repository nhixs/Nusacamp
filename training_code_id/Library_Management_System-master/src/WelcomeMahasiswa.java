    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String sql="select * from skripsi where SkripsiTittle=?";
        try{
           PreparedStatement pst=conn.prepareStatement(sql);
           pst.setString(1, jTextField1.getText());
           rs=pst.executeQuery();
           
         
           
           if(rs.next()){               
               JOptionPane.showMessageDialog(null,
               "Data Skripsi yang anda pilih: "+"\n"
               +"Id Skripsi: "+rs.getInt("idSkripsi")+"\n"
               +"Judul Skripsi: "+rs.getString("SkripsiTittle")+"\n"
               +"Jurusan: "+rs.getString("SkripsiMajor")+"\n"
               +"Kontributor: "+rs.getString("ContributorName")
               +"\n"+"\n"
               +"Silahkan informasikan ke admin Untuk peminjaman");
               
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        } finally{
            try{
               rs.close();
               pst.close();
            }catch(Exception e){
            
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

